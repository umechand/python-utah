coin = int(input(" Enter a coin value"))
if (coin == 1):
        print("penny")
elif (coin == 5):
        print("Nickle")
elif (coin == 10):
        print(" Dime")
elif (coin == 25):
        print("Quarter")
elif (coin == 50):
        print("Half dollar")
else:
        print(" That's not a valid coin!")

